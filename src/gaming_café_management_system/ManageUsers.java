    public void getAllRecords(String email) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<User> list = userDAO.getAllRecords(email);
        Iterator<User> itr = list.iterator();
        while (itr.hasNext()) {
            User userObj = itr.next();
            if (!userObj.getEmail().equals("admin@gmail.com")) {
                // Fixed the missing parenthesis in the addRow method
                dtm.addRow(new Object[]{userObj.getId(), userObj.getName(), userObj.getEmail(), userObj.getPhone(), userObj.getStatus()});
            }
        }
    }

    private void verifyAndUpdateUserStatus() {
        // Get the selected row index
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {
            // Retrieve email and status from the selected row
            TableModel model = jTable1.getModel();
            String email = model.getValueAt(selectedRow, 2).toString();
            String currentStatus = model.getValueAt(selectedRow, 4).toString();

            // Toggle status
            String newStatus = currentStatus.equals("true") ? "false" : "true";

            // Confirm the status change
            int confirmation = JOptionPane.showConfirmDialog(null,
                    "Do you want to change the status of " + email + " to " + newStatus + "?",
                    "Confirm Status Change",
                    JOptionPane.YES_NO_OPTION);

            if (confirmation == JOptionPane.YES_OPTION) {
                // Update the status in the database
                userDAO.changeStatus(email, newStatus);

                // Refresh the table
                getAllRecords("");
            }
        } else {
            // Show a message if no row is selected
            JOptionPane.showMessageDialog(null, "Please select a user to verify!");
        }
    }

